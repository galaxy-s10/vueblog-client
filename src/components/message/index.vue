<template>
  <div class="content">
    <h1 style="text-align:center;padding:10px">留言板</h1>
    <el-divider></el-divider>
    <div style="text-align:center;margin-bottom:30px">这里是一个留言板,想说什么就在这里留言,欢迎吐槽</div>
    <el-input
      type="textarea"
      resize="none"
      :rows="5"
      show-word-limit
      maxlength="200"
      v-model="content"
    ></el-input>
    <p style="text-align:right">
      <el-button type="primary" @click="addcomment()">提交</el-button>
    </p>
    <comment :list="list" :count="count" @reshow="commentlist" />
  </div>
</template>

<script>
import { commentlist, addcomment } from "@/api/comment";
import { format } from "@/utils/format.js";
import comment from "../comment";
export default {
  components: {
    comment
  },
  data() {
    return {
      list: [],
      count: null,
      article_id: -1,
      messagecontent: "",
      isshow: "",
      listLoading: true,
      content: ""
    };
  },
  mounted() {
    this.commentlist(this.article_id);
  },
  beforeRouteEnter(to, from, next) {
    next();
  },
  methods: {
    // 留言列表
    commentlist(id) {
      commentlist(id)
        .then(res => {
          this.list = res.lists;
          this.count = res.count;
        })
        .catch(err => {
          console.log(err);
        });
    },
    // 格式化时间
    format(time) {
      return format(time);
    },
    // 提交留言
    addcomment() {
      if (this.$store.state.user.token) {
        var article_id = this.article_id;
        var from_userid = this.$store.state.user.token;
        var content = this.content;
        var to_commentid = -1;
        var to_username = null;
        var date = new Date();
        var date = format(date);
        addcomment({
          article_id,
          from_userid,
          content,
          to_commentid,
          to_username,
          date
        })
          .then(res => {
            if (res) {
              this.commentlist(this.article_id);
              this.$newmessage("发表成功！", "success");
            } else {
              this.$newmessage(res.data, "success");
            }
          })
          .catch(err => {
            console.log("err");
            this.$newmessage("发表失败！", "error");
          });
      } else {
        this.$newmessage("暂未登录，请登录！", "warning");
      }
    }
  },
  computed: {}
};
</script>


